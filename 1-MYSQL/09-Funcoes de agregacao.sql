--TRAZ VALOR MAXIMO DE UMA COLUNA
SELECT MAX(FEVEREIRO) AS MAIRO_FEV
FROM VENDEREDORES;

--TRAZ VALOR MINIMO DE UMA COLUNA
SELECT MIN(FEVEREIRO) AS MENOR_FEV
FROM VENDEREDORES;

--AVG - TRAZ O VALOR MEDIO
SELECT AVG(FEVEREIRO) AS MEDIA_FEV
FROM VENDEREDORES;

---USANDO VARIAS

SELECT MAX(JANEIRO) AS MAIOR_JAN,
	   MIN(JANEIRO) AS MENOR_JAN,
	   AVG(JANEIRO) AS MEDIA_JAN
FROM VENDEREDORES;

---FUNCAO TRUNCATE ESSA FUNCAO PEDE DOIS PARAMETRO
---TRUNCATE(1 NUMERO, 2 QUANTIDADE DE PARAMETRO);
SELECT MAX(JANEIRO) AS MAIOR_JAN,
	   MIN(JANEIRO) AS MENOR_JAN,
	   TRUNCATE(AVG(JANEIRO),2) AS MEDIA_JAN
FROM VENDEREDORES;

---AGREGANDO COM SUM()

SELECT SUM(JANEIRO) AS TOTAL_JAN,
	   SUM(FEVEREIRO) AS TOTAL_FEV,
       SUM(MARCO) AS TOTAL_MAR
FROM VENDEREDORES;

---VENDAS POR SEXO
SELECT SEXO , SUM(MARCO) AS TOTAL_MARCO
FROM VENDEREDORES
GROUP BY SEXO;


